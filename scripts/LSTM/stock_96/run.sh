#!/bin/bash

DATASET="stock"
PRED_LEN=96
MODEL="LSTM"
CHECKPOINT_DIR="./checkpoints/${MODEL}/${DATASET}_${PRED_LEN}/"
BASE_LR=0.005
WEIGHT_DECAY=0.0001
GATING_INIT=0.01

echo "Running for dataset: ${DATASET}, prediction length: ${PRED_LEN}, model: ${MODEL}"
python main.py DATA.NAME ${DATASET} DATA.PRED_LEN ${PRED_LEN} MODEL.NAME ${MODEL} MODEL.pred_len ${PRED_LEN} TRAIN.ENABLE False TRAIN.CHECKPOINT_DIR ${CHECKPOINT_DIR} TTA.ENABLE False TTA.SOLVER.BASE_LR ${BASE_LR} TTA.SOLVER.WEIGHT_DECAY ${WEIGHT_DECAY} TTA.TAFAS.GATING_INIT ${GATING_INIT}